		in a multitude of media.</p>
		<p>As an advocate of best practise for art in the public realm Sam is regularly 
		invited to speak at arts lead events, she has recently been as a board director 
		of IXIA, the national organisation for public art, having also been its 
		chair for 4 years. Sam has been able to recommend and influence positive 
		changes in the support and commissioning of public art. She also acts as 
		independent advisor to various Councils and their Arts Officers for specific 
		events.</p>
